namespace craft\events;

use craft\ui\ComponentMetadata;
use craft\ui\MountedComponent;
use yii\base\Event;

class ComponentPreRenderEvent extends Event
{
    /** @internal */
    public const EMBEDDED = '__embedded';

    public ?MountedComponent $mounted = null;
    public ?ComponentMetadata $metadata = null;
    public ?array $variables = [];

    public string $template;

    public function init()
    {
        parent::init(); // TODO: Change the autogenerated stub

        $this->template = $this->metadata->template;
    }
}
